/**
 * Builds the CodeMirror WebView editor:
 * 1. Bundles scripts/codemirror-webview-entry.js (and deps) to a single IIFE
 * 2. Inlines it into the HTML template
 * 3. Writes components/codemirror-editor/editor-html.generated.ts for offline use
 */
const fs = require("fs");
const path = require("path");

async function build() {
  let esbuild;
  try {
    esbuild = require("esbuild");
  } catch {
    console.error(
      "esbuild not found. Run: npm install --save-dev esbuild (or npm install --legacy-peer-deps)"
    );
    process.exit(1);
  }

  const projectRoot = path.resolve(__dirname, "..");
  const entryPath = path.join(projectRoot, "scripts", "codemirror-webview-entry.js");
  const templatePath = path.join(projectRoot, "components", "codemirror-editor", "editor-template.html");
  const outPath = path.join(projectRoot, "components", "codemirror-editor", "editor-html.generated.ts");

  const result = await esbuild.build({
    entryPoints: [entryPath],
    bundle: true,
    format: "iife",
    platform: "browser",
    write: false,
    minify: true,
    define: { "process.env.NODE_ENV": '"production"' },
  });

  let bundleCode = result.outputFiles[0].text;
  // Escape for use inside HTML script tag: avoid breaking out of </script>
  bundleCode = bundleCode.replace(/<\/script>/gi, "<\\/script>");

  let html = fs.readFileSync(templatePath, "utf8");
  html = html.replace("__CODEMIRROR_BUNDLE__", bundleCode);

  // Escape backticks and ${ for use inside a TS template literal
  const escaped = html
    .replace(/\\/g, "\\\\")
    .replace(/`/g, "\\`")
    .replace(/\$/g, "\\$");

  const tsContent = `// AUTO-GENERATED by scripts/build-codemirror-editor.js - do not edit
export const CODEMIRROR_EDITOR_HTML = \`${escaped}\`;
`;

  fs.mkdirSync(path.dirname(outPath), { recursive: true });
  fs.writeFileSync(outPath, tsContent, "utf8");
  console.log("Wrote", outPath);
}

build().catch((err) => {
  console.error(err);
  process.exit(1);
});
